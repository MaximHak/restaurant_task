        e.preventDefault();

        var ele = $(this).data("value");

        $.ajax({
            url: '{{ route('add.to.cart') }}',
            method: "get",
            data: {
                _token: '{{ csrf_token() }}',
                id: ele
            },
            success: function (response) {
                toastr.success("Produsul a fost adaugat!");
                var count = $('#count_food').text();
                $('#count_food').text(parseInt(count) + 1);
                $('.cart_button_'+ele).empty();
